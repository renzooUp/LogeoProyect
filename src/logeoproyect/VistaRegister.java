    private void btnVerificarDNIMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnVerificarDNIMouseClicked
                   //codigo para verificar si el DNI esta en uso o no!
            
        try {
            
            String dni,record;
            int conta=0;
            dni=txtDNI.getText(); // aca capturo el valor DNI para comparar d
//            llamando a la base de datos
            BufferedReader br=new BufferedReader(new FileReader("UsuarioRegister.txt"));
            
//                  como lo llamre "br", ahora lo llamo y con "readline" me permite leer la linea de documento
            while( (record=br.readLine()) != null){  // br, "readline" lee la linea e igualalo a la variable "record"
//                el string tokenizer me permite separar los strings  y delimitar las "," 
                StringTokenizer st =new StringTokenizer(record,","); // cada q halles una "," separalo  
                    if(record.contains(dni)){   //si la variable record contiene el numero ingresado en "dni" ejecutalo
                    System.out.println("Nombre: "+st.nextToken()+"   Apellido: "+st.nextToken()+"    DNI: "+st.nextToken()+"   Usuario: "+st.nextToken());
                    
                       conta++;
                    }
                
                }if(conta>0){
                    JOptionPane.showMessageDialog(null,"El usuario con este DNI esta registrado");
                
                }else{
                    
                    JOptionPane.showMessageDialog(null,"DNI no encontrado en la base de datos");
                
                }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(VistaRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(VistaRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnVerificarDNIMouseClicked

