    private void btnRegistrarseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarseMouseClicked
              
        try {
            
            BufferedWriter bw = new BufferedWriter(new FileWriter("UsuarioRegister.txt",true));
            
            String nombre = txtNombre.getText();
            String apellido=txtApellido.getText();
            String dni=txtDNI.getText();
            String user=txtUsuario.getText();
            String contra=txtPass.getText();
            String fecha = txtFecha.getText();
            //Date fecha=calendar.getDate();
            String sexo = "null";
            if(btnMasculino.isSelected()){
                sexo="masculino";
            }            
            if(btnFemenino.isSelected()){
                sexo="femenino";
            }
            JOptionPane.showMessageDialog(null,"Registro de datos exitosos");
            //ahora pasa el filtro para verificar si los 
            //datos ingresados son correctamente
            //*proxima clase*
            bw.write(nombre+","+apellido+","+dni+","+user+","+contra+","+fecha+","+sexo);
            bw.flush();
            bw.newLine();
            bw.close();
            
        } catch (IOException ex) {
            Logger.getLogger(VistaRegister.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Problemas al crear el documento");
        }
    }//GEN-LAST:event_btnRegistrarseMouseClicked
